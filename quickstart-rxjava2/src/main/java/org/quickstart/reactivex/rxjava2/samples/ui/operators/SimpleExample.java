package org.quickstart.reactivex.rxjava2.samples.ui.operators;

import org.quickstart.reactivex.rxjava2.samples.utils.AppConstant;

import io.reactivex.Observable;
import io.reactivex.Observer;
import io.reactivex.disposables.Disposable;
import io.reactivex.schedulers.Schedulers;

/**
 * Created by amitshekhar on 27/08/16.
 */
public class SimpleExample {

  private static final String TAG = SimpleExample.class.getSimpleName();

  public static void main(String[] args) {
    new SimpleExample().doSomeWork();
    for (;;) {
    }
  }

  /*
   * simple example to emit two value one by one
   */
  private void doSomeWork() {
    getObservable()
        // Run on a background thread
        .subscribeOn(Schedulers.io())
        // Be notified on the main thread
        // .observeOn(AndroidSchedulers.mainThread())
        .subscribe(getObserver());
  }

  private Observable<String> getObservable() {
    return Observable.just("Cricket", "Football");
  }

  private Observer<String> getObserver() {
    return new Observer<String>() {

      @Override
      public void onSubscribe(Disposable d) {
        System.out.println(" onSubscribe : " + d.isDisposed());
      }

      @Override
      public void onNext(String value) {
        System.out.println(" onNext : value : " + value);
        System.out.println(AppConstant.LINE_SEPARATOR);
      }

      @Override
      public void onError(Throwable e) {
        System.out.println(" onError : " + e.getMessage());
        System.out.println(AppConstant.LINE_SEPARATOR);
      }

      @Override
      public void onComplete() {
        System.out.println(" onComplete");
        System.out.println(AppConstant.LINE_SEPARATOR);
      }
    };
  }

}
